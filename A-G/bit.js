console.log("Bitwise OR Operator");
console.log(14 | 13); //15
console.log("Bitwise AND Operator");
console.log(14 & 13); //12

console.log("Bitwise XOR Operator");
console.log(14 ^ 13);
console.log("Bitwise Complement Operator");
console.log(~14);
console.log("Left Shift Operator");
console.log(27 << 3);
console.log("Right Shift Operator");
console.log(27 >> 3);
